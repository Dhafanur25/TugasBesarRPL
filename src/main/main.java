    private void updateUIComponentIfNotEmpty(JTextField textField, JLabel label, JButton button) {
        String text = textField.getText().trim();
        if (!text.isEmpty()) {
            label.setText(text);
            if (button != null) {
                button.setText(text);
            }
        }
    }
    private void buttonSimpanPerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanPerActionPerformed
        // TODO add your handling code here:
        
        if (filePath != null){
            profilePicBM.setPic(filePath);
            profilePicW.setPic(filePath);
        }
    
        updateUIComponentIfNotEmpty(isiNamaUP, TitleNama, buttonName);
        updateUIComponentIfNotEmpty(isiEmailUP, TitleEmailBM, null); 
        updateUIComponentIfNotEmpty(isiNoTelpUP, TitleNoTelpBM, null); 
        updateUIComponentIfNotEmpty(isiAlamatUP, TitleAlamatBM, null);
        if (!isiBioUP.getText().equals("")){
            TitleBioBM.setText(isiBioUP.getText());
        }

        isiBioUP.setText("");
        JTextField[] textFields = {isiNamaUP, isiEmailUP, isiNoTelpUP, isiAlamatUP};

        for (JTextField textField : textFields) {
            textField.setText("");
        }
        
        isiGenderUP.setSelectedItem("<Pilih>");
        
        CardLayout contentPane = (CardLayout) getContentPane().getLayout();
        contentPane.show(getContentPane(), "BioMahC");
        

    }//GEN-LAST:event_buttonSimpanPerActionPerformed

    private void buttonUbahFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUbahFotoActionPerformed
        // TODO add your handling code here:
        
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int returnValue = fileChooser.showOpenDialog(this);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            filePath = selectedFile.getAbsolutePath();
        }
        
    }//GEN-LAST:event_buttonUbahFotoActionPerformed

