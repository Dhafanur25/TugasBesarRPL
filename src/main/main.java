    private void buttonRegisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRegisActionPerformed
        // TODO add your handling code here:
        namaL = isiNamaR.getText();
        email = isiEmailR.getText();
        uName = isiUsernameR.getText();
        pass = isiPasswordR.getText();
        
        if (checR.isSelected() && !namaL.equals("") && 
            !uName.equals("") && !pass.equals("")) {
            
            TitleNama.setText(namaL);
            buttonName.setText(namaL);
            TitleEmailBM.setText(email);
            TitleUNameBM.setText(uName);
            
            JTextField[] textFields = {isiNamaR, isiPasswordR, isiEmailR, isiUsernameR};

            for (JTextField textField : textFields) {
                textField.setText("");
            }
            checR.setSelected(false);
            
            CardLayout contentPane = (CardLayout) getContentPane().getLayout();
            contentPane.show(getContentPane(), "LoginC");
        }
    }//GEN-LAST:event_buttonRegisActionPerformed

